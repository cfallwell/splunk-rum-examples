import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = dirname(here);

const readUtf8 = (relativePath) => readFileSync(join(repoRoot, relativePath), "utf8");
const readJson = (relativePath) => JSON.parse(readUtf8(relativePath));
const readBase64 = (relativePath) => readFileSync(join(repoRoot, relativePath)).toString("base64");
const writeUtf8 = (relativePath, value) => writeFileSync(join(repoRoot, relativePath), value);

const signalfxManifest = readJson("spa-npm/src/signalfx/manifest.json");
const localBootstrapVersion = readJson("spa-npm/package.json").version;

const signalfxRumBase64 = readBase64("spa-npm/src/signalfx/splunk-otel-web.min.js");
const signalfxSessionReplayBase64 = readBase64(
  "spa-npm/src/signalfx/splunk-otel-web-session-recorder.min.js"
);

const embeddedSourcesTs = `// Generated by scripts/generate-rum-embeds.mjs
// Source files live in src/signalfx and are downloaded from the SignalFx CDN.

export const LOCAL_RUM_BOOTSTRAP_VERSION = "${localBootstrapVersion}";
export const SIGNALFX_RELEASE = "${signalfxManifest.release}";
export const SIGNALFX_SOURCE = "${signalfxManifest.source}";
export const SIGNALFX_FETCHED_AT = "${signalfxManifest.fetchedAt}";
export const SIGNALFX_RUM_SCRIPT_BASE64 = "${signalfxRumBase64}";
export const SIGNALFX_SESSION_REPLAY_SCRIPT_BASE64 = "${signalfxSessionReplayBase64}";
`;

writeUtf8("spa-npm/src/signalfx/embeddedSources.ts", embeddedSourcesTs);

const mpaTemplate = readUtf8("mpa-script/src/rumbootstrap.template.js");
const mpaOutput = mpaTemplate
  .replace("__LOCAL_RUM_BOOTSTRAP_VERSION__", localBootstrapVersion)
  .replace("__SIGNALFX_RELEASE__", signalfxManifest.release)
  .replace("__SIGNALFX_SOURCE__", signalfxManifest.source)
  .replace("__SIGNALFX_FETCHED_AT__", signalfxManifest.fetchedAt)
  .replace("__SIGNALFX_RUM_SCRIPT_BASE64__", signalfxRumBase64)
  .replace("__SIGNALFX_SESSION_REPLAY_SCRIPT_BASE64__", signalfxSessionReplayBase64);

writeUtf8("mpa-script/rumbootstrap.js", mpaOutput);
