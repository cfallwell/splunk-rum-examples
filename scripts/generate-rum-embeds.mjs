import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = dirname(here);

const readUtf8 = (relativePath) => readFileSync(join(repoRoot, relativePath), "utf8");
const readBase64 = (relativePath) => readFileSync(join(repoRoot, relativePath)).toString("base64");
const writeUtf8 = (relativePath, value) => writeFileSync(join(repoRoot, relativePath), value);

const VENDORED_FROM = "https://cdn.signalfx.com/o11y-gdi-rum/latest";
const VENDORED_AT = "2026-03-16";
const LOCAL_SCRIPT_VERSION = "5.0.0";
const RUM_VENDOR_VERSION = "3.48.0";
const SESSION_REPLAY_VENDOR_VERSION = "2.3.0";

const rumBase64 = readBase64("spa-npm/src/vendor/splunk-otel-web.min.js");
const sessionReplayBase64 = readBase64("spa-npm/src/vendor/splunk-otel-web-session-recorder.min.js");

const embeddedSourcesTs = `// Generated by scripts/generate-rum-embeds.mjs
// Source files live in src/vendor and are vendored from the Splunk CDN.

export const LOCAL_RUM_BOOTSTRAP_VERSION = "${LOCAL_SCRIPT_VERSION}";
export const SPLUNK_RUM_VENDOR_VERSION = "${RUM_VENDOR_VERSION}";
export const SPLUNK_SESSION_REPLAY_VENDOR_VERSION = "${SESSION_REPLAY_VENDOR_VERSION}";
export const SPLUNK_VENDOR_SOURCE = "${VENDORED_FROM}";
export const SPLUNK_VENDOR_FETCHED_AT = "${VENDORED_AT}";
export const SPLUNK_RUM_SCRIPT_BASE64 = "${rumBase64}";
export const SPLUNK_SESSION_REPLAY_SCRIPT_BASE64 = "${sessionReplayBase64}";
`;

writeUtf8("spa-npm/src/vendor/embeddedSources.ts", embeddedSourcesTs);

const mpaTemplate = readUtf8("mpa-script/src/rumbootstrap.template.js");
const mpaOutput = mpaTemplate
  .replace("__LOCAL_RUM_BOOTSTRAP_VERSION__", LOCAL_SCRIPT_VERSION)
  .replace("__SPLUNK_RUM_VENDOR_VERSION__", RUM_VENDOR_VERSION)
  .replace("__SPLUNK_SESSION_REPLAY_VENDOR_VERSION__", SESSION_REPLAY_VENDOR_VERSION)
  .replace("__SPLUNK_VENDOR_SOURCE__", VENDORED_FROM)
  .replace("__SPLUNK_VENDOR_FETCHED_AT__", VENDORED_AT)
  .replace("__SPLUNK_RUM_SCRIPT_BASE64__", rumBase64)
  .replace("__SPLUNK_SESSION_REPLAY_SCRIPT_BASE64__", sessionReplayBase64);

writeUtf8("mpa-script/rumbootstrap.js", mpaOutput);
